import React from 'react';
import { useNavigate } from 'react-router-dom';

const Results = () => {
  const navigate = useNavigate();
  const storedResults = sessionStorage.getItem('analysisResults');
  let results = null;

  try {
    if (storedResults) {
      results = JSON.parse(storedResults);
    }
  } catch (error) {
    console.error('Error parsing results:', error);
  }

  if (!results) {
    return (
      <div className="min-h-screen bg-[#0f1629] py-12 px-4 sm:px-6 lg:px-8 text-white">
        <div className="max-w-3xl mx-auto">
          <div className="bg-[#1a2137] rounded-lg shadow-xl p-6">
            <h2 className="text-2xl font-bold mb-4">No Results Available</h2>
            <p className="mb-4">Please upload an image for analysis first.</p>
            <button
              onClick={() => navigate('/upload')}
              className="inline-flex items-center px-4 py-2 border border-transparent text-sm font-medium rounded-md text-white bg-indigo-600 hover:bg-indigo-700"
            >
              Go to Upload
            </button>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-[#0f1629] py-12 px-4 sm:px-6 lg:px-8">
      <div className="max-w-4xl mx-auto">
        <div className="bg-[#1a2137] rounded-lg shadow-xl p-8">
          <div className="flex justify-between items-center mb-8">
            <h2 className="text-3xl font-bold text-white">Analysis Results</h2>
            <button
              onClick={() => navigate('/upload')}
              className="inline-flex items-center px-4 py-2 border border-transparent text-sm font-medium rounded-md text-white bg-indigo-600 hover:bg-indigo-700"
            >
              Analyze Another Image
            </button>
          </div>

          <div className="space-y-8 text-white">
            {/* Technical Assessment */}
            <div>
              <h3 className="text-xl font-semibold mb-3 text-indigo-400">Technical Assessment</h3>
              <p className="text-gray-300 whitespace-pre-line">{results.technique}</p>
            </div>

            {/* Clinical Findings */}
            <div>
              <h3 className="text-xl font-semibold mb-3 text-indigo-400">Clinical Findings</h3>
              <div className="text-gray-300 whitespace-pre-line">{results.diagnosis}</div>
            </div>

            {/* Patient Summary */}
            <div>
              <h3 className="text-xl font-semibold mb-3 text-indigo-400">Patient Summary</h3>
              <div className="text-gray-300 whitespace-pre-line">{results.explanation}</div>
            </div>

            {/* Recommendations */}
            <div>
              <h3 className="text-xl font-semibold mb-3 text-indigo-400">Recommendations</h3>
              <div className="text-gray-300 whitespace-pre-line">{results.recommendations}</div>
            </div>

            {/* Disclaimer */}
            <div className="mt-8 pt-6 border-t border-gray-700">
              <p className="text-sm text-gray-400 italic">
                Note: This analysis is generated by AI for informational purposes only. 
                Please consult with a qualified healthcare professional for medical advice and treatment decisions.
              </p>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Results;
